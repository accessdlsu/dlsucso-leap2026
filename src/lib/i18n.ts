const LOCALE_MAP: Record<LocaleCode, Strings> = {
  en,
  fil,
  'zh-hans': zhHans,
  'zh-hant': zhHant,
  ko,
  ja,
};

export function getStrings(locale?: LocaleCode): Strings {
  const l = locale ?? getLocale();
  return LOCALE_MAP[l] ?? en;
}

export type StringKey = keyof Strings;

export function t(locale: LocaleCode, key: StringKey, vars?: Record<string, string | number>): string {
  const strings = LOCALE_MAP[locale] ?? en;
  let str = strings[key] as string;
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      str = str.replace(`{${k}}`, String(v));
    }
  }
  return str;
}
